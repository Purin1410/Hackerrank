string kangaroo(int x1, int v1, int x2, int v2) {
    if (v1 == v2) {
        return "NO";
    }
    int ismeet = (x2 - x1) % (v1 - v2);
    if ((x2 - x1) >0 and (v1 - v2) >0 and ismeet == 0) return "YES";
    return "NO";
}


